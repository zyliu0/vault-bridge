# ---------------------------------------------------------------------------
# HandlerResult — output of handle()
# ---------------------------------------------------------------------------

@dataclass
class HandlerResult:
    """Result of processing a single file through handle().

    Attributes:
        text:     Extracted text, or '' if the category doesn't extract text,
                  or None if the file was skipped (unknown extension).
        images:   Extracted / passthrough image paths.
        skipped:  True when the extension is unrecognised or path is empty.
        category: The category slug from HandlerConfig, or 'unknown' when skipped.
    """
    text: Optional[str] = None
    images: List[Path] = field(default_factory=list)
    skipped: bool = False
    category: str = ""


# ---------------------------------------------------------------------------
# Public API
# ---------------------------------------------------------------------------

def get_handler(path: str) -> Optional[HandlerConfig]:
    """Return the HandlerConfig for the given file path, or None if unrecognised.

    Lookup is by the last extension only (.tar.gz → 'gz').
    Case-insensitive. Leading dot removed.
    Returns None for empty paths, paths with no extension, and dot-files
    with no real extension (e.g. '.hidden').

    Args:
        path: File path as string (may be relative or absolute).

    Returns:
        HandlerConfig or None.
    """
    if not path:
        return None

    p = Path(path)
    suffix = p.suffix  # '' or '.ext'
    if not suffix:
        return None

    ext = suffix.lstrip(".").lower()
    if not ext:
        # Dot-file like '.hidden' — suffix is '.hidden', ext is 'hidden'
        # But Path('.hidden').suffix == '' on Python 3.9, so this branch is
        # actually unreachable for dot-files.  Left for clarity.
        return None

    # Dot-file guard: Path('.hidden').stem == '.hidden', .suffix == ''
    # So we only reach here with a real extension.
    return HANDLERS.get(ext)


