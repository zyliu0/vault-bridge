"""File-type handler registry for vault-bridge.

Generated by scripts/generate_file_type_handlers.py — but also usable standalone.

Provides:
  HANDLERS   — dict[str, HandlerConfig] mapping extension → handler config
  get_handler(path) -> HandlerConfig | None
  read_text(path, workdir=None) -> str
  extract_images(path, workdir=None) -> list[Path]
  handle(path, workdir=None) -> HandlerResult

Categories:
  document-pdf      — PDF files (PyPDF2 text + embedded image extraction)
  document-office   — DOCX / PPTX / XLSX (python-docx / python-pptx)
  image-raster      — JPG PNG GIF WEBP BMP TIFF HEIC (Pillow passthrough)
  image-vector      — SVG AI EPS (passthrough; no compression)
  video             — MP4 MOV AVI MKV etc. (skipped — no text/image extraction)
  audio             — MP3 WAV FLAC AAC etc. (skipped)
  text-plain        — TXT MD RST CSV (raw UTF-8 read)
  archive           — ZIP RAR 7Z TAR (skipped)

Delegated categories (v14.3, F1/F6) — dispatched to per-extension handler
modules under `<workdir>/.vault-bridge/handlers/`:
  cad-dxf, cad-dwg, cad-3dm, vector-ai, raster-psd,
  document-office-legacy, spreadsheet-legacy

These are opt-in via /vault-bridge:setup. When the workdir or the handler
file is missing, read_text/extract_images return empty for them rather
than raising — the capability gracefully degrades.

Python 3.9 compatible.
"""
import logging
from dataclasses import dataclass, field
from pathlib import Path
from typing import Dict, List, Optional

import handler_dispatcher

logger = logging.getLogger(__name__)


# ---------------------------------------------------------------------------
# HandlerConfig — immutable descriptor per extension category
# ---------------------------------------------------------------------------

@dataclass(frozen=True)
class HandlerConfig:
    """Descriptor for how vault-bridge should process a given file category.

    Attributes:
        category:       Slug identifying the file category (e.g. 'document-pdf').
        extract_text:   Whether to attempt text extraction from this file type.
        extract_images: Whether to attempt image extraction / passthrough.
        compress:       Whether to compress extracted images (JPEG, strip EXIF).
        run_vision:     Whether to run LLM vision on extracted images.
        render_pages:   Whether the handler renders pages to images (e.g. DXF, AI, PSD).
    """
    category: str
    extract_text: bool
    extract_images: bool
    compress: bool
    run_vision: bool
    render_pages: bool = False


